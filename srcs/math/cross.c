#include "smallpt.h"

t_vec	cross(t_vec a, t_vec b)
{
	t_vec v;

	v[0] = a[1] * b[2] - a[2] * b[1];
	v[1] = a[2] * b[0] - a[0] * b[2];
	v[2] = a[0] * b[1] - a[1] * b[0];
	return (v);
}
