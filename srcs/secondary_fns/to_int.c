#include "smallpt.h"

int	to_int(t_vec v)
{
	t_color	c;

	c.byte[0] = (int)(clamp(v[2]) * 255 + .5);
	c.byte[1] = (int)(clamp(v[1]) * 255 + .5);
	c.byte[2] = (int)(clamp(v[0]) * 255 + .5);
	c.byte[3] = 0x0;
	return (c.val);
}
